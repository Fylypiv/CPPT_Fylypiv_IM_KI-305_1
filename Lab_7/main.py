import sys

def main():
    """
    Головна функція програми.
    Виконує зчитування даних, генерацію зубчастого масиву та виведення матриці.
    """
    
    # --- Введення даних ---
    try:
        n = int(input("Введiть розмiр матрицi n: "))
            # 2. Перевірка, чи число більше 0
        if n <= 0:
            print("Помилка: розмір матриці повинен бути > 0.")
            sys.exit(1)
    except ValueError:
            # Цей блок спрацює, якщо ввели текст ("abc") або дробове число ("5.5")
        print("Помилка: число повинне бути цілим.")
        sys.exit(1)


    fill_char = input("Введiть символ-заповнювач: ")

        # --- Перевірка правильності введення символу ---
        # Вимога: при введенні кількох символів або жодного - переривання
    if len(fill_char) != 1:
        print("Помилка: потрiбно ввести рiвно один символ!")
        sys.exit(1)


    # --- Створення зубчастого списку (Jagged List) ---
    # Цей список буде містити ТІЛЬКИ символи, що потрапляють у заштриховану область
    jagged_list = []

    for i in range(n):
        row = [] # Створюємо новий рядок (список)
        for j in range(n):
            # Верхній трикутник: j >= i and j < n - i
            # Нижній трикутник: j <= i and j >= n - i - 1
            if (j >= i and j < n - i) or (j <= i and j >= n - i - 1):
                row.append(fill_char)
        
        # Додаємо сформований рядок у загальний список
        jagged_list.append(row)

    # --- Виведення результату на екран ---
    print("\nРезультат:")
    
    for i in range(n):
        # Створюємо ітератор для поточного рядка зубчастого списку,
        # щоб брати з нього символи по черзі
        row_iterator = iter(jagged_list[i])
        
        for j in range(n):
            # Перевіряємо ту ж саму умову, щоб знати, де малювати символ, а де пробіл
            if (j >= i and j < n - i) or (j <= i and j >= n - i - 1):
                # next(row_iterator) бере наступний доступний символ із збереженого списку
                print(f"{next(row_iterator)} ", end="")
            else:
                # Виводимо два пробіли для збереження формату таблиці
                print("  ", end="")
        
        print() # Перехід на новий рядок

if __name__ == "__main__":
    main()